// Get word of the day based on date (changes at noon/12:00)
export function getWordOfDay() {
    const startDate = new Date('2025-01-01T12:00:00');
    const now = new Date();
    
    // Calculate the current period (each period is from noon to noon)
    const currentPeriodStart = new Date(now);
    if (now.getHours() < 12) {
        // Before noon, use yesterday's noon
        currentPeriodStart.setDate(currentPeriodStart.getDate() - 1);
    }
    currentPeriodStart.setHours(12, 0, 0, 0);
    
    const diffTime = Math.abs(currentPeriodStart - startDate);
    const diffDays = Math.floor(diffTime / (1000 * 60 * 60 * 24));
    
    return FRENCH_WORDS[diffDays % FRENCH_WORDS.length];
}

// Get random word
export function getRandomWord() {
    return FRENCH_WORDS[Math.floor(Math.random() * FRENCH_WORDS.length)];
}
